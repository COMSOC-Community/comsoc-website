<h1>About COMSOC-2016 ...</h1>


<p>Computational social choice is a rapidly growing discipline at the 
interface  of  social  choice  theory  and  computer  science.  It  is 
concerned with the application  of  computational  techniques  to  the 
study of social choice mechanisms, and with the integration of  social 
choice paradigms into computing.</p>

<p>The <b>Sixth International Workshop on Computational Social Choice
(COMSOC-2016) will take place on June 22&ndash;24, 2016, in Toulouse, France</b>.
It will be hosted by the 
<a href="http://www.irit.fr" target="_blank">Institut de Recherche en Informatique de Toulouse (IRIT)</a>
 of the
<a href="http://www.univ-toulouse.fr" target="_blank">University of Toulouse</a>.
The aim of the workshop is to bring together different communities:
computer scientists interested  in  computational  issues  in 
social choice; people working in artificial  intelligence  and
multiagent systems who are using  ideas  from  social  choice  to  organize 
societies of artificial software agents; logicians interested  in  the 
logic-based specification and analysis of  social  procedures; 
computer science theorists analyzing algorithmic properties of social phenomena;
and last but not least people  coming  from  social  choice 
theory itself: economists, mathematicians, and political scientists.</p>

<p><b>
COMSOC-2016 is temporally co-located with <a href="http://www.nek.lu.se/en/scw16" target="_blank">The 13th Meeting of the Society for Social Choice and Welfare</a>, which will take place on June 28th&ndash;July 1st at Lund University, Sweden.
</p></b>

<br>

<h1>Call for Papers </h1>
<p>
Submissions of papers describing original or recently published work on all aspects of computational social choice are invited. Papers will have to be submitted electronically via the workshop webpage, and accepted papers will be collected in informal workshop notes. COMSOC-2016 will also include a poster session, with selection based on abstract submitted by emails. A full call for papers is available on the COMSOC-2016 webpage: <a href="http://www.irit.fr/COMSOC-2016/cfp.php">www.irit.fr/COMSOC-2016/cfp.php</a>
</p>

<p>
For additional information please consult the <b>workshop webpage</b>: <a href="http://www.irit.fr/COMSOC-2016">www.irit.fr/COMSOC-2016</a>
</p>

