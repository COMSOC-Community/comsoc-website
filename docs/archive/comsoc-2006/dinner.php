<h1>Social Dinner</h1>

<p>
A social dinner is planned for Thursday night (7 December 2006).
We'll be going to an Indonesian restaurant to have <i>rijstafel</i>, 
by many considered to be the pinnacle of (colonial) Dutch cuisine.
The literal translation of <i>rijstafel</i> would be "rice table":
so there'll be <i>rice</i>, yes, as well a whole collection of  
small dishes covering the entire <i>table</i> (typically ranging from 
things such as spicy beef and grilled chicken, 
over vegtables in peanut sauce, to fried coconut powder).
</p>

<p>
The cost per dinner ticket is &euro;30 
(to be paid together with the registration fee). 
