dishes, and wine. To book a place for the dinner, please tick the 
appropriate box on the registration form. There is also a vegetarian 
option (please indicate on the form). 
</p>

<p>
The restaurant is called <i>Puri Mas</i> and the address is 
<i>Lange Leidsedwarsstraat 37-41</i>. This is just off <i>Leidseplein</i> 
(tram lines 1, 2, 5, 7 and 10), which can be found in the lower 
lefthand corner of <a href="amsterdam.pdf">this map</a>. 
Dinner starts at 20:00.
</p>

