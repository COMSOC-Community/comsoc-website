<b>Budget-Balance in Social Choice</b>
</p>

<p>
Known general mechanisms for incentive-compatible social choice such
as the Clarke tax generate budget surplus. While game theory stipulates 
that such surplus must be wasted, in practice it is usually given to 
an interested party, thus creating incentives for manipulation. 
The talk will discuss possibilities for achieving
budget-balance in social choice mechanisms. 
