<h1>Paper Submission</h1>

<p>
<i>Thanks to everyone who submitted. We have received 48 papers. Submission is now closed.</i>
</p>

<p>
Authors are invited to submit full papers not exceeding 14 pages in length (roughly 5000 words).
Each paper should include a title, the names and contact details of all authors, and 
a short abstract of 100-300 words. When preparing your submission, please follow these 
<a href="submission/sample.pdf">formatting instructions</a>. The easiest way of 
doing this is to use the <a href="http://www.latex-project.org">Latex</a> document 
preparation system with the class file <a href="submission/comsoc.cls">comsoc.cls</a>.
We have prepared a sample file (<a href="submission/sample.tex">sample.tex</a>), 
which you can use as a starting point for your own paper.
</p>

<p>
Paper submission is electronic. To submit, please prepare a PDF file 
of your paper, a short abstract in plain text, and a list of two to five 
keywords. Then follow this link:
<center>
<a href="http://www.easychair.org/COMSOC2006/">
Enter the COMSOC-2006 Paper Submission and Reviewing System!</a>
</center>
</p>

<p>
You will be able to revise your submission any number of times before 
the deadline. Please feel free to <a href="contact.php">contact</a> 
the organisers with any questions you may have. 
</p>

