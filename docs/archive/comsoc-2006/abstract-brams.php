<p>
<b>Better Ways to Cut a Cake</b>
</p>

<p>
Procedures to divide a cake among <i>n</i> people with <i>n</i>-1 cuts (the minimum number) 
are analyzed and compared.  For 2 persons, cut-and-choose, while envy-free and efficient, 
limits the cutter to exactly 50% if he or she is ignorant of the chooser's preferences, 
whereas the chooser can generally obtain more.  By comparison, a new 2-person surplus 
procedure (SP), which induces the players to be truthful in order to maximize their 
minimum allocations, leads to a <i>proportionally equitable</i> division of the surplus -- 
the part that remains after each player receives 50% -- by giving each person exactly 
the same proportion of the surplus as he or she values it.
For <i>n</i> &ge; 3 persons, a new equitable procedure (EP) yields a 
<i>maximally equitable</i> division of a cake.  This division gives all players the 
highest common value that they can achieve and induces truthfulness, but it may not be 
envy-free. The applicability of SP and EP to the fair division of a heterogeneous, 
divisible good, like land, is briefly discussed. 
</p>

<p>
This is joint work with Michael A. Jones and Christian Klamler.
</p>

