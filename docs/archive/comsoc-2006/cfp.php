<h1>Call for Papers</h1>

<p>
<i>This Call for Papers is also available in
<a href="cfp/3rd-cfp-comsoc-2006.txt">text</a> format.
For printing, please use the <a href="comsoc-flyer.pdf">flyer</a>.</i>
</p>

<p>Submissions of papers describing original or recently  published  work 
on all aspects of <a href="background.php">computational social choice</a> 
are invited.  Topics  of interest include, but are not limited to:

<ul>
<li>complexity-theoretic analysis of voting procedures</li>
<li>computational aspects of fair division</li>
<li>cake-cutting algorithms</li>
<li>distributed negotiation in multiagent system</li>
<li>preference representation in combinatorial domains</li>
<li>computational aspects of preference aggregation rules</li>
<li>preference elicitation</li>
<li>social choice and constraint programming</li>
<li>social choice and the web: ranking systems</li>
<li>belief and judgement aggregation</li>
<li>computational aspects of coalition formation</li>
<li>social choice under uncertainty</li>
<li>logics for collective decision making</li>
<li>logic-based verification of social procedures</li> 
<li>communication complexity of social choice mechanisms</li>
<li>computational issues in mechanism design</li>
</ul>

Paper <a href="submission.php">submission</a> 
will be electronic via this website (deadline: 1 October 2006).  
Accepted  papers  will  be  collected  in informal workshop notes, 
printed copies of which will be available  at the workshop. 
Please <a href="contact.php">contact</a> either one of the 
programme chairs should you have any questions.
</p>

