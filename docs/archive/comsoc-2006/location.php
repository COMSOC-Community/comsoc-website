<h1>Location</h1>

<p>
COMSOC-2006 will take place at the <i>Roeterseiland</i> campus
of the University of Amsterdam, just east of the city centre. 
Talks and coffee breaks will be organised
in the <i>Euclides</i> building (also known as the P building).
The street address is <i>Plantage Muidergracht 24</i>.
All talks will be held in room P.227 on the second floor.
Registration and coffee breaks will take place in various
rooms on the ground floor (P.017 on Wednesday, P.015A on
Thursday, and P.016 on Friday).
</p>

<p>
Anywhere in central Amsterdam is within easy reach of the workshop venue.
The closest tram stop is on <i>Plantage Middenlaan</i>, near Amsterdam Zoo 
(known as <i>Artis</i>). Lines 9 and 14 stop here (the former coming 
straight from Amsterdam Centraal station).
For the real Amsterdam experience, however, you should 
consider renting a bike (bring some gloves, it's December after all, but
otherwise this really is the most convenient way of getting around town).
Taxis tend to be rather expensive in Amsterdam.
The airport (<i>Schiphol</i>) is well-connected to anywhere in the world
and only a 15 minute train ride away from Amsterdam Centraal station.
</p>

<p>
There are several hotels within walking distance (say, 10 minutes)
of both the workshop venue and some of the tourist attractions in the 
centre (but any other hotel or hostel close to the centre will do
as well). We do not want to make any specific recommendations, 
but you can browse several hotels at 
<a href="http://www.bookings.nl/searchresults.html?city=-2140479">
this</a> website.
</p>


